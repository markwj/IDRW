def beep(dev):
    BUFFER_SIZE = 256
    buff = [0x00] * BUFFER_SIZE

    # Set up payload for beep
    buff[0x00] = 0x01
    buff[0x06] = 0x08
    buff[0x08] = 0xaa
    buff[0x0a] = 0x03
    buff[0x0b] = 0x89
    buff[0x0c] = 0x01
    buff[0x0d] = 0x01
    buff[0x0e] = 0x8a
    buff[0x0f] = 0xbb

    # Write to Feature Report 1
    return  dev.ctrl_transfer(0x21, 0x09, 0x0301, 0, buff)